def sum_two_numbers(a:float, b:float = 5) -> float:
    """ Sum two values.

    Take two variables and sum.

    Args:
        a (float): First number.
        b (float, optional): Second number. Defaults to 5.

    Returns:
        float: The sum of 'a' and 'b'
    """

    number = a + b

    return number